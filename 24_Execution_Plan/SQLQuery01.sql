/* ==============================================================================
  Execution Plan
------------------------------------------------------------------------------- 

The Execution Plan is a vital tool for performance tuning in SQL Server. 
It acts as a roadmap, showing the exact steps the database engine takes to execute a query. 
Reading Direction: You must read the execution plan from right to left and from top to bottom. */

/* ==============================================================================
  Plan Types in SQL Server Management Studio (SSMS):
-------------------------------------------------------------------------------

-> Estimated Plan: The optimizer's best guess before the query is run (no actual query execution).

-> Actual Plan: The plan actually used to execute the query, including statistics like row counts and execution time.

-> Live Query Statistics: Shows the plan and its progress in real-time during execution. */